    public void profilCheck() {
        DefaultComboBoxModel combobox = (DefaultComboBoxModel) ComboBoxProfil.getModel();
        try {
            ResultSet lesDonnees = SqlManager.getInstance().requeteSelection("select * from profil");
            while (lesDonnees.next()) {
                ProfilCombo profil = new ProfilCombo(lesDonnees.getString("id"), lesDonnees.getString("libelle"));
                combobox.addElement(profil);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
